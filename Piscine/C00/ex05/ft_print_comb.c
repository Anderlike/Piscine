#include <unistd.h>

void	ft_print_comb(void)
{
	int	x;
	int	z;
	int	y;

	x = '0';
	while (x <= '7')
	{
		z = x + 1;
		while (z <= '8')
		{
			y = z + 1;
			while (y <= '9')
			{
				write(1, &x, 1);
				write(1, &z, 1);
				write(1, &y, 1);
				if (x != '7')
					write(1, &", ", 2);
				y++;
			}
			z++;
		}
		x++;
	}
}
